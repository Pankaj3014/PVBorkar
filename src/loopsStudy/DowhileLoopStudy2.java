package loopsStudy;

public class DowhileLoopStudy2 {

	public static void main(String[] args) 
	{
		//I want 4 table in reverse order
		// initialization--> 40
		// condition------->a>=4
		// updation--------> a=a-4
		
		int a=40;
		
		do
		{
			System.out.println(a);
			a=a-4;
			
		}
		while(a>=4);
		

	}

}
